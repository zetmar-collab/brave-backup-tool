function Initialize-I18n {
    if (-not $Script:Lang) { $Script:Lang = 'pl' }
}

function T([string]$Key) {
    Initialize-I18n
    $bag = $Script:Strings[$Script:Lang]
    if ($bag.ContainsKey($Key)) { return $bag[$Key] }
    $fb = $Script:Strings['en']
    if ($fb.ContainsKey($Key)) { return $fb[$Key] }
    return $Key
}

function Tf {
    param(
        [Parameter(Mandatory)][string]$Key,
        [Parameter()][object[]]$FormatValues
    )
    $fmt = T $Key
    $vals = @($FormatValues)
    if ($vals.Count -eq 0) { return $fmt }
    return [string]::Format($fmt, $vals)
}

function Get-GuiLangToggleLabel {
    if ($Script:Lang -eq 'pl') { return 'EN' } else { return 'PL' }
}

function Set-AppLanguage([string]$Lang) {
    if ($Lang -ne 'pl' -and $Lang -ne 'en') { return }
    $Script:Lang = $Lang
    Save-BackupSettings
}
